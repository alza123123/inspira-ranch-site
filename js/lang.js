let currentLang = localStorage.getItem('ir_lang') || 'en';

function applyLang(lang) {
  currentLang = lang;
  document.querySelectorAll('[data-key]').forEach(el => {
    const key = el.getAttribute('data-key');
    if (translations[key] && translations[key][lang]) {
      el.textContent = translations[key][lang];
    }
  });
  document.querySelectorAll('[data-key-placeholder]').forEach(el => {
    const key = el.getAttribute('data-key-placeholder');
    const t = translations[key];
    if (t && t[lang]) el.placeholder = t[lang];
  });
  // Legacy CSS class system for pages using .lang.en / .lang.es spans
  document.body.classList.remove('show-en', 'show-es');
  document.body.classList.add('show-' + lang);
  const btn = document.getElementById('langBtn');
  if (btn) btn.textContent = lang === 'en' ? '🌐 Español' : '🌐 English';
  document.documentElement.lang = lang;
  localStorage.setItem('ir_lang', lang);
}

function toggleLang() {
  applyLang(currentLang === 'en' ? 'es' : 'en');
}

document.addEventListener('DOMContentLoaded', () => applyLang(currentLang));
